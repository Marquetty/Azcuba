package models;

/**
 *
 * @author Lenovo
 */
public class usuario {
    private int ci;
    private String nombre;
    private String rol;

    public int getCi() {
        return ci;
    }

    public void setCi(int ci) {
        this.ci = ci;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    public usuario(int ci, String nombre, String rol) {
        this.ci = ci;
        this.nombre = nombre;
        this.rol = rol;
    }
    
}
