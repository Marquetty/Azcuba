    public String edad() {
        String operador = null;
        if (jComboBox1.getSelectedItem().toString().equals("mayor que")) {
            operador = ">";
        } else if (jComboBox1.getSelectedItem().toString().equals("menor que")) {
            operador = "<";
        } else if (jComboBox1.getSelectedItem().toString().equals("mayor igual que")) {
            operador = ">=";
        } else if (jComboBox1.getSelectedItem().toString().equals("menor igual que")) {
            operador = "=<";
        } else {
            operador = "=";
        }
        return operador;
    }

